    public void loadTableJadwal() {
        DefaultTableModel model = new DefaultTableModel();
        model.addColumn("ID");
        model.addColumn("Pasien");
        model.addColumn("Jam");
        model.addColumn("Status");

        try {
            // Query untuk mengambil jadwal hari ini saja
            String sql = "SELECT j.id_jadwal, p.nama, j.jam, j.status " +
                         "FROM jadwal_terapi j " +
                         "JOIN pasien p ON j.id_pasien = p.id_pasien " +
                         "WHERE j.tanggal = CURDATE()";
            
            java.sql.Connection conn = koneksi.Koneksi.sambung();
            java.sql.Statement st = conn.createStatement();
            java.sql.ResultSet rs = st.executeQuery(sql);

            while (rs.next()) {
                model.addRow(new Object[]{
                    rs.getString("id_jadwal"),
                    rs.getString("nama"),
                    rs.getString("jam"),
                    rs.getString("status")
                });
            }
            tblJadwal.setModel(model);
        } catch (Exception e) {
            System.out.println("Error load data: " + e.getMessage());
        }
    }

